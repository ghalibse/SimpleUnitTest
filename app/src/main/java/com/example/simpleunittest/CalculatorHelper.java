package com.example.simpleunittest;


public class CalculatorHelper {


    public int sumItems(int n1, int n2) {

        return n1 + n2;
    }

    public int multiplyItems(int n1, int n2) {

        return n1 * n2;
    }

    public int powerItems(int n1, int n2) {


        return (int) Math.pow(n1, n2);
    }
}
